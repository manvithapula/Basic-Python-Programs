# Define complex numbers
complex_num1 = 3 + 4j
complex_num2 = 2 - 2j

# Addition
addition_result_0001 = complex_num1 + complex_num2
print(f"Addition: {addition_result_0001}")

# Subtraction
subtraction_result_0001 = complex_num1 - complex_num2
print(f"Subtraction: {subtraction_result_0001}")

# Multiplication
multiplication_result_0001 = complex_num1 * complex_num2
print(f"Multiplication: {multiplication_result_0001}")

# Division
division_result_0001 = complex_num1 / complex_num2
print(f"Division: {division_result_0001}")
